public class OperadoresAritmeticos {
    public static void main(String[] args) {
        int n1 = 3;
        int n2 = 5;
        float m = (n1 + n2) / 2;
        System.out.println("A média é igual a: " + m);

        int numero = 10;
        int valor = 4 + --numero;
        System.out.println(valor);
        System.out.println(numero);
    }
}
